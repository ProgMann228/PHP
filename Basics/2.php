          Если оба условия выполняются, заказ получает скидку.

Входные данные:
          Массив заказов, где каждый заказ представлен в виде строки с двумя числами, разделенными пробелом: количество блюд и общая сумма заказа. Данные вводятся пользователем через readline(). Каждая строка соответствует одному заказу. Ввод продолжается до тех пор, пока пользователь не введет пустую строку.

Выходные данные:
          Для каждого заказа программа должна выводить, получает ли заказ скидку или нет.

